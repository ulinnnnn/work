package com.chat.model;

public class ChatPicVO {
	String chat_pic_no;
	String chat_pic_number;
	byte[] chat_img;
	public String getChat_pic_no() {
		return chat_pic_no;
	}
	public void setChat_pic_no(String chat_pic_no) {
		this.chat_pic_no = chat_pic_no;
	}
	public String getChat_pic_number() {
		return chat_pic_number;
	}
	public void setChat_pic_number(String chat_pic_number) {
		this.chat_pic_number = chat_pic_number;
	}
	public byte[] getChat_img() {
		return chat_img;
	}
	public void setChat_img(byte[] chat_img) {
		this.chat_img = chat_img;
	}
	
}
